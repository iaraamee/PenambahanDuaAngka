    private void btnKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKeluarActionPerformed
        //System.exit(0);
        int choice = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin keluar dari aplikasi?", "Konfirmasi Keluar", JOptionPane.YES_NO_OPTION);
        
        if (choice == JOptionPane.YES_OPTION) {
            // Tindakan yang akan diambil jika pengguna memilih "Ya" 
            //System.out.println("Anda telah memilih untuk keluar dari aplikasi."); 
            JOptionPane.showMessageDialog(rootPane, "Aplikasi akan ditutup");
            System.exit(0); // Keluar dari aplikasi
        } else {
            // Tindakan yang akan diambil jika pengguna memilih "Tidak" 
            //System.out.println("Anda membatalkan keluar dari aplikasi.");
            JOptionPane.showMessageDialog(rootPane, "Aplikasi tidak jadi ditutup");
            // Lanjutkan dengan menjalankan aplikasi atau lakukan tindakan lain sesuaiÂ kebutuhan.
        }
    }//GEN-LAST:event_btnKeluarActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        double nilai1, nilai2, hasil;
        nilai1 = Double.parseDouble(tfPertama.getText());
        nilai2 = Double.parseDouble(tfKedua.getText());
        hasil = nilai1 + nilai2;
        //tfhasil.setText(Double.toString(hasil));
        tfHasil.setText(String.valueOf(hasil));
    }//GEN-LAST:event_btnTambahActionPerformed

